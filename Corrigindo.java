package atividades1303;

public class Corrigindo {

	public static void main(String[] args) {
		//Variável e corrigir uma letra
		String errado = "Tedro Tescava Teixes no Tíer";
		String corrigirT = errado.replace("T", "P");

		//Saída
		System.out.println("Antes: " + errado + " Depois: " + corrigirT);
	}

}
